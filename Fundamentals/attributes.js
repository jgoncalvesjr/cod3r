const a = 7;
let b = 3;

b += a; // atribuição ADIÇÃO
console.log(b);

b -= 4; // atribuição SUBTRAÇÃO
console.log(b);

b *= 2; // atribuição MULTIPLICAÇÃO
console.log(b);

b /= 3; // atribuição DIVISÃO
console.log(b);

b **= 2; // atribuição POTENCIAÇÃO - novidade ES7
console.log(b);

b %= 2; // atribuição MÓDULO (par é 0, ímpar é 1)
console.log(b);
